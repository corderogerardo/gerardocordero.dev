// Generates src/study/content/<subject>.ts from a study-content workflow result.
// The workflow authors + fact-checks cards; this turns its JSON into clean, typed,
// hand-editable modules (the TS files are the source of truth thereafter).
//
//   node scripts/gen-study-content.mjs <path-to-workflow-output.json>
//
// Re-run only to regenerate from a fresh workflow; manual edits to the .ts files win.
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");

// Display metadata per subject id (controls picker label/blurb regardless of the
// label the workflow returned). Add an entry when you add a subject.
const META = {
  "react-native": { label: "React Native", short: "RN", blurb: "Architecture, rendering, performance, native modules." },
  ios: { label: "iOS (Swift)", short: "iOS", blurb: "Swift, memory, concurrency, UIKit & SwiftUI." },
};

const FILE = {
  "react-native": { path: "src/study/content/react-native.ts", varName: "reactNativeSubject" },
  ios: { path: "src/study/content/ios.ts", varName: "iosSubject" },
};

const LEVELS = new Set(["junior", "mid", "senior", "architect", "beyond"]);

// Decode the few HTML entities the model emitted despite the plain-text spec, and
// assert nothing HTML-shaped survives (these strings render in RN, not a browser).
function clean(s) {
  let out = String(s);
  for (let i = 0; i < 2; i++) {
    out = out
      .replace(/&amp;/g, "&")
      .replace(/&lt;/g, "<")
      .replace(/&gt;/g, ">")
      .replace(/&quot;/g, '"')
      .replace(/&#0?39;/g, "'")
      .replace(/&#x27;/gi, "'")
      .replace(/&apos;/g, "'")
      .replace(/&nbsp;/g, " ");
  }
  return out;
}

const inPath = process.argv[2];
if (!inPath) {
  console.error("usage: node scripts/gen-study-content.mjs <workflow-output.json>");
  process.exit(1);
}

const parsed = JSON.parse(readFileSync(inPath, "utf8"));
const payload = parsed.result ?? parsed; // tolerate {result:{...}} or bare {...}
const subjects = payload.subjects ?? [];

const warnings = [];
const seen = new Set();
let written = 0;

for (const subj of subjects) {
  const meta = META[subj.id];
  const file = FILE[subj.id];
  if (!meta || !file) {
    warnings.push(`no META/FILE mapping for subject "${subj.id}" — skipped`);
    continue;
  }

  const cards = [];
  for (const c of subj.cards ?? []) {
    const id = clean(c.id).trim();
    if (!id) { warnings.push(`${subj.id}: card with empty id dropped`); continue; }
    if (seen.has(id)) { warnings.push(`duplicate id "${id}" dropped`); continue; }
    seen.add(id);

    const level = LEVELS.has(c.level) ? c.level : "senior";
    const card = {
      id,
      category: clean(c.category).trim(),
      categoryLabel: clean(c.categoryLabel).trim(),
      question: clean(c.question).trim(),
      answer: clean(c.answer).trim(),
      level,
    };
    for (const [k, v] of Object.entries(card)) {
      if (typeof v === "string" && /<\/?[a-z][^>]*>|&[a-z#0-9]+;/i.test(v)) {
        warnings.push(`${id}.${k}: residual HTML/entity -> ${v.slice(0, 60)}`);
      }
    }
    if (!card.question || !card.answer || !card.category) {
      warnings.push(`${id}: missing required field(s) — kept but check`);
    }
    cards.push(card);
  }

  const obj = {
    id: subj.id,
    label: meta.label,
    short: meta.short,
    blurb: meta.blurb,
    cards,
  };

  const header =
    `// ${meta.label} interview deck. Typed data is the source of truth — edit directly.\n` +
    `// Generated by scripts/gen-study-content.mjs from the study-content workflow, then\n` +
    `// fact-checked. Hand-edits are preserved; re-run the generator only to regenerate.\n` +
    `import type { Subject } from "../types";\n\n` +
    `export const ${file.varName}: Subject = `;
  const body = JSON.stringify(obj, null, 2);
  writeFileSync(join(ROOT, file.path), header + body + ";\n", "utf8");
  written += cards.length;
  console.log(`wrote ${file.path}: ${cards.length} cards`);
}

if (warnings.length) {
  console.log("\nwarnings:");
  for (const w of warnings) console.log("  - " + w);
}
console.log(`\ntotal cards written: ${written}`);
